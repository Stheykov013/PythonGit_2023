# Metodo  
    # parametro.upper() ## .upper() es el metodo
    
nombre = "Felipe Gutierrez"

def mostrar_nombre(nombre):
    print(f"hola soy {nombre}")
    
mostrar_nombre(nombre)   

print(nombre.lower()) # Combierte lo que hay en nombre en minuscula
print(nombre.capitalize()) # Combierte lo que hay en nombre con la primera letra en mayuscula
print(nombre.upper()) # Combierte lo que hay en nombre en mayusculas
     